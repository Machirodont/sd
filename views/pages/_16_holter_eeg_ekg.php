 <p>Электроэнцефалография (ЭЭГ) – высокоинформативный метод диагностики состояния нервной системы, основанный на
 регистрации биоэлектрических потенциалов коры головного мозга (ГМ) в процессе его жизнедеятельности.</p>
 <p>Датчики на голове пациента располагают так, чтобы зафиксировать активность всех отделов мозга. В результате по
 электроэнцефалограмме можно проанализировать работоспособность ГМ в целом, выявить патологические очаги, оценить
 характер и степень повреждений, поставить точный диагноз, определить тактику лечения или осуществлять контроль
 эффективности проводимой терапии.</p>
 <p>ПоказанияЭЭГ достаточно широко применяется для диагностики психических, неврологических и психоневрологических
 заболеваний, входит в программу скрининговых обследований при прохождении медкомиссии.</p>
 <p>Электроэнцефалографию назначают при подозрении на такие заболевания, как:</p>
 <ul>
 <li>эпилепсия;</li>
 <li>вегето-сосудистая дистония;</li>
 <li>задержка речевого развития, заикание;</li>
 <li>воспалительные, токсические и дегенеративные поражения головного мозга;</li>
 <li>черепно-мозговые травмы;</li>
 <li>сосудистые патологии и нарушения кровообращения;</li>
 <li>опухолевые процессы в головном мозге.</li>
 </ul>
 <p>Также ЭЭГ может быть назначена лечащим врачом при жалобах пациента на:</p>
 <ul>
 <li>хронические головные боли, в том числе мигренеподобные;</li>
 <li>головокружения и частые обмороки;</li>
 <li>нарушения сна, бессонницу или беспричинные пробуждения по ночам.</li>
 </ul>
 <p>Противопоказаний к проведению ЭЭГ нет.</p>
 <p>Подготовка</p>
 <p>За 48 часов до процедуры следует отказаться от употребления спиртных напитков и кофеинсодержащих продуктов (кофе,
 кола, черный чай, энергетики, шоколад и др.).</p>
 <p>В случае приема нейролептиков и снотворных препаратов необходимо предупредить об этом лечащего врача, чтобы при
 расшифровке электроэнцефалограммы их действие было учтено.</p>
