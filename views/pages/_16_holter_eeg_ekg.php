<h1>Холтер, ЭЭГ (Электроэнцефалограмма)</h1>
<p><strong>Холтер</strong> – аппаратный метод диагностики кардиологических заболеваний, характеризующийся
 круглосуточным мониторированием и записью электрокардиограммы пациента, при помощи закрепления портативного
 устройства на теле пациента.</p>
<p>
