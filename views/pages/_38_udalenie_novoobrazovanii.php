<p><strong>Папиллома</strong>&nbsp;– это доброкачественная опухоль человека и животных, которая образуется на
    поверхности кожи и представляет собой небольшое образование в виде соединительнотканного сосочка (papilla — сосок,
    лат), покрытого эпителием. Папилломы могут возникнуть у человека в любом возрасте, либо развиваться с рождения в
    виду хронического воспалительного раздражения. Внешне папилломы выглядят как выросты эластичной консистенции от
    одного до нескольких миллиметров в диаметре. Окраска варьирует от телесного до почти черного цвета. Локализация
    преимущественно на шее, туловище, в местах естественных складок, однако нередко папилломы можно встретить на
    слизистых оболочках рта, носа и половых органов. Папилломы могут распространяться по телу с необыкновенной
    скоростью, и наличие множественных папиллом уже носит патологический характер и называется папилломатозом. Папилломы
    необходимо лечить на ранней стадии их проявления. Наиболее эффективным сегодня является удаление папиллом лазером.
</p>
