<p><strong>Бородавки</strong>&nbsp;— доброкачественные, весьма распространенные образования на коже. Их появление
    вызывается вирусом. Заразиться ими можно через предметы обихода. Как правило, они не причиняют беспокойства, и
    удаляют их, преследуя исключительно эстетические цели. Один из новейших и популярных способов — лазерное удаление
    бородавок.</p>
<p>Не секрет, что подобные новообразования кардинально и резко могут испортить внешний вид. Ведь они могут появиться в
    любом месте. Некоторым людям бородавки доставляют и болезненные ощущения. С помощью лазера от этой проблемы можно
    избавиться навсегда.</p>
