<p>Ассиметрия — А (от англ. Asymmetry):&nbsp;Половинки невуса друг с другом не совпадают.<br>
    Границы — В (от англ. Border):&nbsp;Края образования размытые, зазубренные или неровные.<br>
    Цвет — С (от англ. Color):&nbsp;Цвет образования неравномерный. Присутствуют различные оттенки коричневого или
    серого, с включениями розовых, красноватых, белых или голубоватых пятен.<br>
    Диаметр — D (от англ. Diameter):&nbsp;Диаметр пятна превышает 6 мм, но некоторые меланомы могут быть меньшего
    размера.</p>
