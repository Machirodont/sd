<p>Раньше удаление родинок или бородавок представляло собой сложную хирургическую процедуру (удаление жидким азотом или
    замораживание, электрокоагуляция, удаление скальпелем). А результат такой процедуры был, как правило,
    неустойчив.</p>
<p>В 80 случаях из 100 ее приходилось повторять.</p>
<p>С приходом в медицину лазера, все изменилось. И из проблемной операции удаление бородавок или папиллом превратилось
    быструю и эффективную процедуру. Эта процедура дает устойчивый результат и позволяет избежать рецидива
    заболевания.</p>
<p>После удаления новообразования&nbsp;необходимо провести гистологические исследования удаленных тканей.</p>
<p>Поэтому в&nbsp;&nbsp;Столичной диагностике используется комбинированная методика лечения новообразований на коже:
    радиоволновой метод, позволяющий получить материал для гистологического исследование с минимальной травматизацией
    тканей</p>
<p>и лазервапоризация, минимизирующая риск рецидивов</p>
<h3>Меланома</h3>
<p>При первых признаках появления у вас на лице и теле подозрительных образований&nbsp;необходимо срочно обратиться к
