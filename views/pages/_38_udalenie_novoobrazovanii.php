<h1>Удаление новообразований (родинки, бородавки)</h1>
<h4>Что представляют собой родинки и бородавки</h4>
<p><strong>Родинки (невусы)</strong>&nbsp;– это приобретенные в течение жизни или врожденные доброкачественные
    пигментные новообразования на коже.</p>
