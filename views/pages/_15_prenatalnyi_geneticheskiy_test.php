            <div> Существующие программы скрининга могут лишь указать на вероятность (например, 1 из 50 или 1 из 5000)
                того, что беременность протекает с хромосомной патологией. При биохимическом скрининге, 1/6 случаев
                синдрома Дауна остается не выявленным. В то же время, среди женщин, имеющих положительные результаты
                биохимического скрининга (где риск синдрома Дауна выше общепопуляционного), 95% вынашивают здорового
                ребенка, подвергаясь ненужному беспокойству и угрозе выкидыша в результате амниоцентеза. Благодаря
                методике неинвазивного пренатального теста, можно будет уточнить с точностью до 99,9% наличие или
                отсутствие у ребенка хромосомной патологии по 13, 18 и 21 хромосоме и моносомии Х хромосомы.
            </div>
