            <div>Столичная диагностика предлагает Вашему вниманию новое уникальное неинвазивное исследование для
                диагностики наиболее распространенных хромосомных синдромов, таких как<strong> синдром Дауна (трисомия
                    21 хромосомы)</strong>, <strong>синдром Эдвардса (трисомия 18 хромосомы)</strong> или <strong>синдром
                    Патау (трисомия 13 хромосомы)</strong>, <strong>синдром Тернера (моносомия Х-хромосомы, ХО)</strong>
                с высокой точностью и минимальным риском для себя и будущего ребенка.
            </div>
            <div> Новая методика предназначена для уточнения выявленной возможной хромосомной патологии плода по данным
                УЗИ и данным биохимических рисков, также если в семье есть ребенок с хромосомным синдромом. Анализ также
                рекомендуется проводить беременным старше 35 лет и в качестве скринингового теста для диагностики
                трисомий 13,18,21 хромосом и моносомии Х хромосомы.
            </div>
            <div><a href="/uploads/2015/05/ДОТ-тест-3.jpg"><img alt="ДОТ тест 3"
                                                                src="/uploads/2015/05/ДОТ-тест-3-300x248.jpg"
