                                                                sizes="(max-width: 300px) 100vw, 300px" width="300"
                                                                height="248"></a></div>
            <div> Неинвазивный пренатальный тест является безопасной альтернативной методикой амниоцентезу, поэтому не
                вызывает опасности прерывания беременности. По сравнению с другими исследованиями, позволяет получить
                более точную информацию (точность выше 99,9%), чем оценка вероятности наличия хромосомных синдромов с
                помощью биохимического скрининга.
            </div>
            <div><a href="/uploads/2015/05/ДОТ-тест-4.jpg"><img alt="ДОТ тест 4"
                                                                src="/uploads/2015/05/ДОТ-тест-4-300x238.jpg"
                                                                srcset="/uploads/2015/05/ДОТ-тест-4-300x238.jpg 300w, /uploads/2015/05/ДОТ-тест-4.jpg 911w"
                                                                sizes="(max-width: 300px) 100vw, 300px" width="300"
                                                                height="238"></a></div>
            <div> Технология неинвазивного пренатального теста заключается в выделении из крови беременной женщины ДНК
                плода и выполнения анализа с помощью новейшей технологии секвенирования, которая позволяет определить
                ряд тяжелых и наиболее распространенных хромосомных синдромов. Проведенные медицинские исследования
                показали, что наиболее эффективным методом скрининга трисомии 21 хромосомы является исследование
                внеклеточной ДНК, выделенной из плазмы крови матери. Результаты исследований были опубликованы в ведущих
                журналах для акушеров и гинекологов. (Nicolaides et al, Prenatal Diagnosis,2013)
            </div>
            <div><a href="/uploads/2015/05/ДОТ-тест-5.jpg"><img alt="ДОТ тест 5"
