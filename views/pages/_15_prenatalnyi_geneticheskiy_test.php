            <div>
                <div> Для выполнения исследования необходима кровь матери (правила взятия см. ниже).</div>
                <div> Предоставление биоматериала ДНК отца (буккальный соскоб) является желательным для снижения
                    вероятности редких случаев, требующих перезабора материала матери.
                </div>
                <div><strong>Ограничения:</strong></div>
                <div><img alt="" src="http://dialab.ru/images/logo/voskl.png">Неинвазивный Пренатальный Тест является
                    информативным исследованием, <strong>начиная с 9 недели одноплодной беременности</strong>.
                    Используется только при прогрессирующей одноплодной беременности. Не проводится для пациенток ЭКО
                    при суррогатном материнстве и при использовании донорской яйцеклетки. Также недопустимо проведение
                    исследования у пациенток с пересадками костного мозга и/или стволовых клеток в анамнезе.
                </div>
                <div><strong>Срок выполнения:</strong> до 12 рабочих дней</div>
                <p><strong>ИНСТРУКЦИЯ ПО СБОРУ БИОМАТЕРИАЛА на Неинвазивный Пренатальный Тест </strong></p>
                <div>
                    <div>Сдать кровь на «Неинвазивный Пренатальный Тест» можно независимо от времени приема пищи.</div>
                    <div>За 20 минут до сдачи крови можно съесть что-нибудь сладкое (шоколад, сладкий сок), что повысит
                        концентрацию фетальной ДНК («свободно циркулирующей ДНК плода»).
                    </div>
                    <div>
                        <div><strong>Кровь пациентки</strong></div>
                        <div>1. Взять кровь пациентки в две 10 мл пробирки, поставляемые в наборе. Всего необходимо
                            собрать не менее 16 мл крови.
                        </div>
                        <div>2. Сразу после забора крови пробирку перевернуть 10 раз для перемешивания крови с
                            реактивом. Пробирки не встряхивать!
                        </div>
                        <div>3. Пробирки промаркировать, обернуть гелевой упаковкой и поместить в металлизированный
                            конверт.
                        </div>
                        <div></div>
                        <div>
                            <hr>
                        </div>
                        <div>
                            <div><em><strong>Формат записи паспортных данных на пробирке </strong></em></div>
                            <div>Сначала идёт ФАМИЛИЯ («Last name»), а затем ИМЯ («First name»), чётко и разборчиво –
                                всё печатными заглавными латинскими буквами.
                            </div>
                            <div>В поле DOB MM/DD/YYYY (дата рождения) следует писать последовательно:</div>
                            <div>а) сначала 2 цифры месяца «ММ»</div>
                            <div>б) затем 2 цифры числа месяца «DD»</div>
                            <div>в) затем 4 цифры года «YYYY»</div>
                        </div>
                    </div>
                </div>
                <div>
                    <div><em><strong>Пример:</strong></em></div>
                    <div>пациентка Мария Петровна Иванова, дата рождения 21 декабря 1983 года,</div>
                    <div>записывается следующим образом: IVANOVA MARIA DOB 12/21/1983</div>
                    <div><em>Предложите Пациентке самой записать свои Фамилию и Имя на английском так, как записано в
                            загранпаспорте или на банковской карточке, а только после этого процедурная сестра
                            воспроизведёт всё на пробирке. Пусть пациентка лично напишет в бланке ФИО полностью, дату
                            рождения и проверит, как процедурная сестра воспроизвела эти данные на пробирках, а затем
                            поставит рядом в бланке свою роспись. </em></div>
                        <hr>
                    <div>
                        <div></div>
                        <div>4. Наклеить 1 (один) штрих-код в верхний правый угол направления, а оставшиеся 2 (два)
                            оставить в наборе.
                        </div>
                        <div>5. Поместить металлизированный конверт с образцами крови в набор Natera. Набор с собранными
                            образцами хранить при комнатной температуре.
                        </div>
                        <div>6. Доставка образцов в лабораторию строго в день взятия материала</div>
                        <div>
                            <p><strong>Инструкция по забору образца букального эпителия (ДНК биологического
                                    отца)</strong></p>
                            <ul>
                                <li>Не есть в течение 30 мин до взятия мазка.</li>
                                <li>Прополоскать рот чистой водой.</li>
                                <li>Чётко записать информацию на этикетке с образцом букального эпителия с именем и
                                    датой рождения отца (формат записи такой же, как и в случае с кровью матери,
                                    латинскими буквами)
                                </li>
                                <li>Вскрыть тубу с тампоном и снять колпачок.</li>
                                <li>Вынуть палочку с ватным тампоном из тубы и энергично потереть тампоном вперёд-назад
                                    по внутренней поверхности щеки в течение 60 секунд. Движения должны быть с усилием,
                                    но без болезненных ощущений.
                                </li>
                                <li>Вернуть палочку с тампоном в оригинальную тубу и положить на ровную поверхность для
                                    высыхания. Не закрывать колпачок.
                                </li>
                                <li>Оставить тампон на 15 минут. Затем плотно закрыть колпачок тубы.</li>
                            </ul>
