<p>Для вычисления рисков используется специальное программное обеспечение. Простое определение уровня показателей в
    крови недостаточно для того, чтобы решить, повышен риск аномалий развития, или нет. Программное обеспечение должно
    быть сертифицировано для использования с целью пренатального скрининга. На первом этапе компьютерного обсчета цифры
    показателей, полученные при лабораторной диагностике, переводятся в так называемые MoM (multiple of median, кратное
    медианы), характеризующие степень отклонения того или иного показателя от медианы. На следующем этапе расчета
    производится поправка MoM на различные факторы (масса тела женщины, расовая принадлежность, наличие некоторых
    заболеваний, курение, многоплодная беременность и т. д.). В результате получаются так называемые скорректированные
    MoM. На третьем этапе расчета скорректированные MoM используются для расчета рисков. Программное обеспечение
    специальным образом настраивается под используемые в лаборатории методы определения показателей и реактивы.
    Недопустимо рассчитывать риски с использованием анализов, сделанных в другой лаборатории. Наиболее точным расчет
    рисков аномалий плода бывает при использовании данных ультразвукового исследования, выполненного в 10-13 недель
    беременности.</p>
<p>Что такое MoM?</p>
<p>MoM – английская аббревиатура термина “multiple of median”, что в переводе означает “кратное медианы”. Это
    коэффициент, показывающий степень отклонения значения того или иного показателя пренатального скрининга от среднего
    значения для срока беременности (медианы). MoM расчитывается по следующей формуле:</p>
<p>MoM = [Значение показателя в сыворотке крови пациентки] / [Значение медианы показателя для срока беременности]</p>
<p>Поскольку значение показателя и медиана имеют одни и те же единицы измерения, значение MoM не имеет единиц измерения.
    Если значение MoM у пациентки близко к единице, то значение показателя близко к среднему в популяции, если выше
    единицы – выше среднего в популяции, если ниже единицы – ниже среднего в популяции. При врожденных пороках плода
    могут быть статистически значимые отклонения MoM маркеров. Однако в чистом виде MoM почти никогда не используются в
    расчете рисков аномалий плода. Дело в том, что при наличии целого ряда факторов, средние значения MoM отклоняются от
    средних в популяции. К таким факторам относятся масса тела пациентки, курение, расовая принадлежность, наступление
    беременности в результате ЭКО и др. Поэтому после получения значений MoM программа расчета рисков делает поправку на
    все эти факторы, в результате чего получается так называемое “скорригированное значение MoM”, которое и используется
    в формулах расчета рисков. Поэтому в бланках заключения по результатам анализа рядом с абсолютными значениями
    показателей указываются скорригированные значения MoM для каждого показателя.</p>
