        <h2>Зачем Нужна Допплерометрия и кто такой Допплер?</h2>
        <div></div>
        <div>
            <p>Один из общепризнанных методов пренатальной диагностики —&nbsp;Допплеровское исследование&nbsp;(допплерометрия,
                допплерография) — является разновидностью УЗИ. Эта процедура безболезненна и безвредна как для матери,
                так и для плода.</p>
            <p>Допплерометрия проводится одновременно с УЗИ.</p>
            <p>Историческая справка:</p>
            <p>В основе допплерометрии лежит&nbsp;эффект Допплера&nbsp;— изменение частоты звука при отражении от
                движущегося объекта. В биологических средах таким объектом является кровь внутри сосудов. Впервые это
                явление было открыто Христианом Допплером в 1842 году. А первое использование допплерометрии в
                акушерстве было предпринято в 1977 году для оценки кровотока в пупочной артерии (Fitzgerald and Drumm.
                Non-invasive measurement of human circulation using ultrasound: a new method. BMJ 1977). В последующие
                годы широкое использование допплерометрии в клинической практике позволило значительно снизить процент
                таких серьезных осложнений, как тяжелое течение поздних гестозов, нефропатия, фетоплацентарная
                недостаточность, задержка внутриутробного развития и внутриутробная гибель плода. Снизилась частота
                осложнений в родах (дистресс-синдром, асфиксия плода).</p>
            <h3>Безопасность ультразвука</h3>
            <p>По приказу N 572н в РФ Скрининговое ультразвуковое исследование проводится трехкратно: при сроках
                беременности 11-14 недель, 18-21 неделю и 30-34 недели. В первую очередь, для выявления пороков
                развития.</p>
            <p>Современные УЗ аппараты отображают термальный индекс TI и механический индекс MI</p>
            <p>Безопасным, не нарушающем миелинизацию нервных волокон и др. эффекты, для плода являются значения меньше
                1,0</p>
            <p>Самый безопасный-черно-белый 2 д режим, далее по нарастанию УЗ нагрузки на плод:</p>
            <p>Цветной допплер (когда вы видите цвет), Пульсовой допплер (когда вы слышите звук)</p>
            <p>Чем дольше проводится исследование, тем большую нагрузку получает плод</p>
            <p>Количество сигналов, производимых и получаемых датчиками существенно не менялось за последние 10 лет,
                улучшается только качество обработки данных полученного сигнала</p>
            <p>При 3Д реконструкции нагрузка не вырастает, но увеличивается время исследования.</p>
            <p>Поэтому время исследования зависит от качества получаемого изображения, уровня подготовки врача УЗИ
                (начинающий врач тратит в 2-3 раза больше времени на выведение), положения плода и веса беременной
                женщины</p>
            <p>Среднее время исследования не должно превышать 15 минут, но следует стремится к сокращению этого времени
                (принцип ALARA — настолько мало, насколько возможно для сохранения информативности)</p>
            <p>Условия безопасного УЗИ</p>
            <p>-УЗИ аппарат экспертного уровня, не старше 5 лет</p>
            <p>— Опыт специалиста не менее 5 лет при выполнении не менее 100 исследований в месяц и международный
                сертификат FMF для выполнения 1-го скрининга (указывается в заключении)</p>
            <p>— Сокращение использования допплерометрии, особенно в головном мозге (достаточно 3-4 пульсовых волн),
                исключить исследование, где дают послушать сердцебиение в сроке до 12 недель</p>
            <p>— Обращать внимание на TI и MI на видеозаписи вашего исследования</p>
            <p>— Если 3Д картинка не получилась — лучше не продлевать время исследования, а поменять положение тела (в
                США использование 3Д в развлекательных коммерческих целях запрещено)</p>
            <p>— Использование трансвагинального датчика, у пациенток с лишним весом</p>
            <h3>Кардиотокография (КТГ)</h3>
            <p>Сердечная деятельность плода является наиболее точным и объективным показателем состояния плода в анте- и
                интранатальном периодах. Нарушения сердечной деятельности могут быть признаком страдания плода во время
                беременности или родов. Объективной задачей пренатальной диагностики, предупреждающей смерть плода и
                тяжелые неврологические нарушения является раннее выявление страдания и выбор оптимального времени и
                срока родоразрешения.</p>
