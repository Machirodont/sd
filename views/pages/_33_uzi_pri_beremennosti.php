<p>При исследовании в 10-14 недель&nbsp;можно обнаружить грубые дефекты развития – аненцефалию, омфалоцеле, шейную
    гигрому и некоторые другие. Также определяют маркер хромосомных и ряда нехромосомных аномалий развития плода –
    толщину воротникового пространства (ТВП, синонимы – воротниковая зона, шейная складка). Нормальное значение ТВП –
