<h1>УЗИ при беременности</h1>
<h4>1-й скрининг</h4>
<p>Доказательная база:</p>
<p>При исследовании 5000 пациенток у 275 диагностирован высокий риск, после проведения НИПТ или Амниоцентеза — Tr21
    подтвердился в 13 случаев, в то время как в случае низкого риска диагностировано только 2 случая Tr21. Т.е.
    чувствительность не очень высокая.</p>
<p>Для увеличения чувствительности исследуют дополнительные УЗ маркеры, УЗ нагрузка растет, а у плода идет наиболее
    активная стадия развития мозга.</p>
<p>После 35 лет, риск будет повышен даже при хороших результатах УЗИ и биохимического скрининга</p>
<p>Беременная женщина испытывает большое волнение, пока ждет результаты. И этот результат не однозначный, только
    вероятность…</p>
<p>Поэтому, если возраст старше 35 лет или позволяет бюджет, лучше перед 1-м скринингом проводить Неинвазивный
    пренатальный тест, тогда не придется сдавать биохимические маркеры и объем УЗ исследования, а следовательно УЗ
    нагрузку, можно сократить</p>
