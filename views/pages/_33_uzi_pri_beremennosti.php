<p>Что касается&nbsp;биохимического скрининга, то набор показателей будет различным в разные сроки беременности. В сроки
    беременности&nbsp;10-13 недель&nbsp;проверяются следующие показатели:</p>
<ul>
    <li>свободная β-субъединица хорионического гормона человека (св. β-ХГЧ)</li>
    <li>PAPP-A (pregnancy associated plasma protein A), связанный с беременностью плазменный протеин A</li>
</ul>
<p>Расчет риска измерения аномалий плода, проведенный на основании измерения этих показателей, называется&nbsp;двойным
    биохимическим тестом первого триместра беременности.</p>
<p>Стандарты ультразвукового сканирования в конце первого триместра беременности разрабатываются базирующейся в Англии
    организацией Fetal Medicine Foundation (FMF). В Столичной диагностике УЗИ проводится по протоколу FMF.</p>
