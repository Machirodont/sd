<p><em><strong>Жидкостная цитология</strong></em>&nbsp;— высокотехнологичный и, в настоящее время, один из самых точных
    метод выявления и дифференциации заболеваний шейки матки.</p>
<p>Используется метод жидкостной цитологии для обнаружения атипичных и раковых клеток шейки матки или предшествующего им
    патологического изменения клеток эпителия.</p>
<p><img src="/uploads/2015/05/жидкостная-цитология-2-229x300.jpg"
        alt="жидкостная цитология 2" width="229" height="300"
        srcset="/uploads/2015/05/жидкостная-цитология-2-229x300.jpg 229w, /uploads/2015/05/жидкостная-цитология-2.jpg 615w"
        sizes="(max-width: 229px) 100vw, 229px"></p>
<p><strong>Данный метод имеет ряд преимуществ:</strong></p>
<ul>
    <li>в контейнер попадает весь материал</li>
    <li>позволяет получить чрезвычайно тонкий слой клеток, обеспечивая сохранность клеточных структур</li>
    <li>тонкослойные препараты удобны для микроскопии</li>
    <li>клетки сохраняют морфологические, молекулярно-биологические свойства</li>
    <li>уменьшает фон, позволяет избежать «загрязнения» опухолевых клеток элементами крови и воспаления, облегчая
        просмотр цитологических препаратов.
    </li>
    <li>лучше дифференцируются зрелые и незрелые клетки, а также клетки, пораженные вирусом.</li>
    <li>длительный срок хранения исследуемого материала.</li>
    <li>архивирование данных и возможность возврата к протоколу исследования в любое время</li>
</ul>
<p>Применение жидкостной цитологии и иммуноцитохимического определения онкомаркера p16ink4a играет существенную роль для
    скрининга, диагностики и выбора тактики лечения заболеваний шейки матки.</p>
<p>При раке и различной степени неоплазии шейки матки уровень р16 повышается, что обусловлено участием белка Е7 вируса
    папилломы человека.</p>
