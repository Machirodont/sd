<p><strong>Преимущества данного теста:</strong></p>
<ul>
    <li>разрешает все спорные вопросы при выявлении гинекологом высокоаномальной кольпоскопической картины, не
        сопровождающейся изменениями в цитологическом мазке
    </li>
    <li>достоверно оценивается потенциал дисплазии в отношении развития рака шейки матки и, соответственно, появляется
        выбор в более консервативной или более агрессивной тактике лечения
    </li>
    <li>при проведении совместно с цитологическим исследованием, совершенствуется мониторинг пациенток, прошедших
        органосохраняющее лечение по поводу тяжелой дисплазии или carcinomae in situ.
    </li>
</ul>
