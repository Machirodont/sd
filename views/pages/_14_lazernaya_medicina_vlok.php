<h1>Лазерная медицина. Фотодинамическая терапия. ВЛОК.</h1>

<p><strong>ВЛОК</strong> (внутривенное лазерное облучение крови) — это одна из самых эффективных разновидностей
    современной физиотерапии, без которой сегодня медики почти не представляют себе лечение обширного списка
    заболеваний. До недавнего времени эта методика считалась экспериментальной, но высокая лечебная результативность
    ВЛОК очень быстро была признана медицинским сообществом. Все без исключения аппараты «Матрикс» прошли сертификацию.
</p>
