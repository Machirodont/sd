Кожа, защищающая внутренние системы и&nbsp;органы человека от&nbsp;внешней среды, сама также нуждается в&nbsp;должном уходе и&nbsp;защите, поскольку кожный покров постоянно подвергается агрессивным внешним воздействиям и&nbsp;инфекциям (вирусы, бактерии, грибы и&nbsp;пр.), что приводит к развитию в ней аномальных изменений. Кроме того, кожа связана с&nbsp;внутренними системами организма, и&nbsp;поэтому поражающие их&nbsp;нарушения могут стать причиной патологических процессов на&nbsp;коже. Кожные покровы отражают состояние организма и&nbsp;могут сигнализировать о&nbsp;возникших внутренних заболеваниях в&nbsp;виде различных высыпаний. Поэтому для лечения дерматологических проблем зачастую приходится привлекать врачей других направлений: эндокринологов, гастроэнтерологов, терапевтов,&nbsp; гинекологов и пр. Волосы, потовые и&nbsp;сальные железы, ногти также подвержены патологическим процессам.
Некоторые кожные болезни, хотя и не связаны с проблемами внутренних органов, доставляют и физические, и душевные страдания, т.к. вынуждают человека ограничивать круг общения, отказываться от многих привычных занятий и даже приводят к инвалидности.
Среди самых часто встречающихся&nbsp;заболеваний кожи&nbsp;выделяют угри&nbsp;(акне), экзему,&nbsp;псориаз,&nbsp; различные&nbsp;дерматиты, грибковые заболевания, простой герпес, бородавки. Несмотря на то, что кожа является самым доступным человеческим органом, диагностика её болезней значительно затруднена их огромным разнообразием. В первую очередь, необходим общий осмотр кожных покровов, позволяющий установить первый предварительный диагноз. В процессе осмотра учитывается общее состояние кожи больного, патологические процессы кожи, их характер. Для подтверждения диагноза в дерматологии обычно применяются дополнительные методы исследования: микроскопия, дерматоскопия, различные кожные тесты, биохимические анализы и др.
