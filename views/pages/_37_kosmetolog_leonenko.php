Как подготовиться к посещению специалиста?
В первую очередь, необходимо принять гигиенический душ и подготовить чистое белье. Непосредственно перед посещением врача не стоит принимать никаких противоаллергических препаратов. Так же стоит отказаться от средств наружного использования, так как все эти препараты могут исказить реальную картину заболевания, что приведет к неправильному диагнозу и всем остальным негативным последствиям.
Дерматология&nbsp;– наука, изучающая строение кожи, её физиологические функции, причины и течение&nbsp;кожных болезней, методы их диагностики, лечения и профилактики.
Заболевания кожи&nbsp;— дерматозы — известны с древнейших времён (3000 лет до н. э.). Отдельные описания кожных болезней, методов их лечения встречаются в египетских папирусах, китайских манускриптах, русских летописях и др.
Кожа&nbsp;– особый орган тела человека, ее площадь у взрослого человека составляет от 1,5 до 2,5 м2. Она состоит из трех плотно прикрепленных друг к другу слоев: эпидермиса, дермы и подкожной жировой клетчатки. К придаткам (или производным) кожи относят кожные железы (потовые и сальные), а также волосы и ногти.
