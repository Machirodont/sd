<h1>Электрокардиограмма (ЭКГ)</h1>
<div align="justify"> <b>Электрокардиограмма (ЭКГ)</b> (electrocardiogram (ECG)) — графическая запись электрических потенциалов, сопровождающих работу сердца, на движущейся бумажной ленте. Запись электрокардиограммы производится с помощью специального прибора, называемого электрокардиографом (electrocardiograph).
