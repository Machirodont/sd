<div align="justify"> <b>Показания к проведению ЭКГ</b><br>
 </div>
<p></p>
<div align="justify">
    <ul>
        <li> Подозрение на заболевания сердца и высокий риск в отношении этих заболеваний. </li>
        <li> Ухудшение состояния больных с заболеваниями сердца, появление болей в области сердца, развитие или усиление одышки, возникновение аритмии. 
        </li>
        <li> Перед любыми оперативными вмешательствами. </li>
        <li> Заболевания внутренних органов, эндокринных желез, нервной системы, болезней уха, горла, носа, кожные заболевания и т. д. при подозрении на вовлечение сердца в патологический процесс. 
        </li>
        <li> Наличие профессионального риска. </li>
    </ul>
    <p> С помощью электрокардиограммы врач может диагностировать различные заболевания сердца, приводящие к характерным изменениям на ЭКГ.<br>
  <br>
Предварительной записи или специальной подготовки к исследованию не требуется.  </p></div>
